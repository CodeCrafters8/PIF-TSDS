class User:
    def __init__(self, id_inversor, cuil, nombre, apellido, email, contraseña, saldo_pesos, perfil_inversor_id):
        self.id_inversor = id_inversor
        self.cuil = cuil
        self.nombre = nombre
        self.apellido = apellido
        self.email = email
        self.contraseña = contraseña
        self.saldo_pesos = saldo_pesos
        self.perfil_inversor_id = perfil_inversor_id